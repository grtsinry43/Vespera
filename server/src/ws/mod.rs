//! WebSocket 实时推送模块
//!
//! 提供基于 WebSocket 的实时数据推送功能,让前端无需轮询即可获得:
//! - 节点状态实时更新
//! - 告警事件实时推送
//! - 节点上线/下线事件
//!
//! # 架构
//! ```text
//! ┌─────────────┐     ┌──────────────┐     ┌─────────────┐
//! │   Agent     │────>│    Server    │<────│  Frontend   │
//! │             │ HTTP│  (HTTP API)  │ WS  │  (Browser)  │
//! └─────────────┘     │              │     └─────────────┘
//!                     │  Broadcast   │
//!                     │   Channel    │
//!                     └──────────────┘
//!                            │
//!                     ┌──────┴──────┐
//!                     ▼             ▼
//!             ┌────────────┐ ┌────────────┐
//!             │ WS Client1 │ │ WS Client2 │
//!             └────────────┘ └────────────┘
//! ```
//!
//! # 性能
//! - 每个 WS 连接: ~10KB 内存
//! - 广播通道: ~100KB (缓冲 1000 条消息)
//! - 100 节点 + 10 前端: ~1.1MB 内存, < 2% CPU
//!
//! # 安全
//! - 连接建立后 5 秒内必须完成 JWT 认证
//! - 支持节点订阅过滤 (用户只能看到有权限的节点)
//! - 心跳检测 (30 秒 ping/pong)

pub mod broadcaster;
pub mod handler;

pub use broadcaster::Broadcaster;
pub use handler::ws_handler;
